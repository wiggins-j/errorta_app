"""Deterministic Markdown report renderer.

``render_markdown(aggregation, metadata)`` returns a stable string given
stable inputs. The only timestamp embedded is ``metadata["generated_at"]``
if the caller provides it — nothing else reads ``datetime.now()``.
"""
from __future__ import annotations

from typing import Any, Mapping

from .aggregator import AggregationResult


def _fmt_float(value: float | None) -> str:
    if value is None:
        return "n/a"
    return f"{value:.4f}"


_FAKE_BANNER = (
    ">  **FAKE DATA — phase-aware deterministic simulation. Not for "
    "v0.3 launch claims.**\n>\n> This report was generated by "
    "`scripts/run-benchmark.sh --fake --simulate-corrections` against the\n"
    "> `DeterministicMockClient`. **No real judge ran.** Verdicts are a "
    "deterministic\n> function of `(prompt_id, phase)` across four phases — "
    "`primary`,\n> `paraphrase`, `after_correction_primary`, "
    "`after_correction_paraphrase` —\n> with a reserved ~30% "
    "hallucination-wedge subset that always fails on the\n> primary phase "
    "until included in the simulated corrected_ids list, plus a\n> synthetic "
    "`similar` grounding-match neighborhood that lights up on the\n> "
    "after-correction paraphrase pass. The numbers below illustrate the "
    "shape\n> of the wedge story; only `--real` (requires "
    "`ERRORTA_REAL_BENCHMARK=1` and\n> a healthy judge) produces claims that "
    "can ship."
)

_METHODOLOGY = (
    "## Methodology\n"
    "\n"
    "The harness loads a YAML seed of prompts (id, text, paraphrase,\n"
    "expected topics) and drives a **three-phase** flow against the\n"
    "sidecar:\n"
    "\n"
    "1. **Primary pass.** Each prompt is POSTed to `/judge/verdict`.\n"
    "   The rating (`pass` / `partial` / `fail` / `uncertain` / `error`) and any\n"
    "   `grounding_match` block are recorded.\n"
    "2. **Wedge amplification (REAL mode only).** A configurable\n"
    "   subset (~30% by default) of failed or partial primaries are\n"
    "   pushed to `/judge/accept` with a synthetic correction. This\n"
    "   seeds the grounding store so the paraphrase pass can reground\n"
    "   semantically similar prompts. Requires\n"
    "   `ERRORTA_GROUNDING_EMBEDDINGS=1`.\n"
    "3. **Paraphrase pass.** With `--re-run-paraphrase` the paraphrase\n"
    "   variant of each prompt is issued. The **F024 paraphrase delta**\n"
    "   (pass-rate-on-paraphrase minus pass-rate-on-primary, over the\n"
    "   matched prompt-id intersection) is computed alongside the new\n"
    "   **F024 grounding amplification** metrics: count, rate, and\n"
    "   mean-score delta over the subset of paraphrase verdicts whose\n"
    "   `grounding_match.kind == \"similar\"`.\n"
    "\n"
    "**FAKE vs REAL semantics.** FAKE mode (`--fake`, the default in CI)\n"
    "uses `DeterministicMockClient`; ratings are a stable hash of the\n"
    "prompt text and `grounding_match` is never returned, so the\n"
    "amplification phase is skipped and similar-match metrics are zero /\n"
    "`n/a`. REAL mode (`--real`, requires `ERRORTA_REAL_BENCHMARK=1`)\n"
    "uses a live `httpx.Client` against the sidecar; only in REAL mode\n"
    "with `ERRORTA_GROUNDING_EMBEDDINGS=1` does the wedge actually fire.\n"
    "\n"
    "Aggregation is pure: `median_score` over recorded scores (pass=1.0,\n"
    "partial/uncertain=0.5, fail/error=0.0), `pass_rate` as the\n"
    "fraction with rating == `pass`, and per-rating counts. The\n"
    "renderer is deterministic given fixed metadata — `generated_at` is\n"
    "the only timestamp embedded; it comes from\n"
    "`ERRORTA_BENCHMARK_NOW` when set."
)


def render_markdown(
    aggregation: AggregationResult,
    metadata: Mapping[str, Any] | None = None,
    *,
    is_fake_run: bool = False,
) -> str:
    meta = dict(metadata or {})
    title = str(meta.get("title") or "Errorta benchmark report")
    seed = str(meta.get("seed") or "welcome_v1")
    generated_at = str(meta.get("generated_at") or "")
    notes = str(meta.get("notes") or "")
    seed_prompt_count = meta.get("seed_prompt_count")
    # F-DEMO-01 Slice (b) Run metadata fields. All optional; the renderer
    # only emits the "Run metadata" subsection when at least one is set,
    # so fake-mode renderings stay byte-identical to before this change.
    judge_model = meta.get("judge_model")
    ollama_version = meta.get("ollama_version")
    aiar_pin_source = meta.get("aiar_pin_source")
    seed_sha256 = meta.get("seed_sha256")

    counts = aggregation.rating_counts
    # Deterministic ordering for counts.
    count_rows = "\n".join(
        f"| {rating} | {counts[rating]} |" for rating in sorted(counts)
    ) or "| (none) | 0 |"

    matched = ", ".join(aggregation.matched_prompt_ids) or "(none)"


    lines: list[str] = []
    if is_fake_run:
        lines += [_FAKE_BANNER, ""]
    lines += [
        f"# {title}",
        "",
        f"- seed: `{seed}`",
        f"- generated_at: `{generated_at}`",
        f"- total verdicts: {aggregation.total}",
        "",
    ]
    # F-DEMO-01 Slice (b) "Run metadata" subsection. Only rendered when at
    # least one provenance field is present, so the byte-stable test in
    # tests/test_benchmark_aggregator.py keeps passing for legacy callers
    # that don't populate any of these.
    run_meta_rows: list[tuple[str, str]] = []
    if judge_model is not None:
        run_meta_rows.append(("judge_model", str(judge_model)))
    if ollama_version is not None:
        run_meta_rows.append(("ollama_version", str(ollama_version)))
    if aiar_pin_source is not None:
        run_meta_rows.append(("aiar_pin_source", str(aiar_pin_source)))
    if seed_sha256 is not None:
        run_meta_rows.append(("seed_sha256", str(seed_sha256)))
    if run_meta_rows:
        lines += [
            "## Run metadata",
            "",
            "| field | value |",
            "| --- | --- |",
        ]
        lines += [f"| {k} | `{v}` |" for k, v in run_meta_rows]
        lines += [""]
    lines += [
        _METHODOLOGY,
        "",
        "## Metrics",
        "",
        "| metric | value |",
        "| --- | --- |",
        f"| median_score | {_fmt_float(aggregation.median_score)} |",
        f"| pass_rate | {_fmt_float(aggregation.pass_rate)} |",
        f"| F024_paraphrase_delta | {_fmt_float(aggregation.F024_paraphrase_delta)} |",
        f"| before_after_delta | {_fmt_float(aggregation.before_after_delta)} |",
        "",
        "## Rating counts",
        "",
        "| rating | count |",
        "| --- | --- |",
        count_rows,
        "",
        "## F024 Grounding Amplification",
        "",
        "| metric | value |",
        "| --- | --- |",
        f"| f024_similar_match_count | {aggregation.f024_similar_match_count} |",
        f"| f024_similar_match_rate | {_fmt_float(aggregation.f024_similar_match_rate)} |",
        (
            "| f024_similar_match_score_delta | "
            f"{_fmt_float(aggregation.f024_similar_match_score_delta)} |"
        ),
        (
            "| f024_similar_match_mean_similarity | "
            f"{_fmt_float(aggregation.f024_similar_match_mean_similarity)} |"
        ),
        "",
        "## Matched prompt ids (before/after delta basis)",
        "",
        matched,
    ]
    if notes:
        lines += ["", "## Notes", "", notes]

    # Provenance footnote — surface the seed prompt count for auditability.
    prompt_count_str = (
        str(seed_prompt_count)
        if seed_prompt_count is not None
        else str(len({v for v in aggregation.matched_prompt_ids}) or aggregation.total)
    )
    lines += [
        "",
        "---",
        "",
        (
            f"[^provenance]: Seed file `{seed}` contains "
            f"**{prompt_count_str} prompts**. Each prompt was issued once as "
            "the primary; with `--re-run-paraphrase` the paraphrase variant "
            "was also issued, doubling the verdict count. The verdict log is "
            "persisted alongside this report for audit."
        ),
    ]
    lines.append("")  # trailing newline
    return "\n".join(lines)
